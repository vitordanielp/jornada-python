numero_1 = 21
numero_2 = 100
numero_3 = 0.5


""" Operações básicas """

# Soma
print(numero_1 + numero_2)  # 121
# Subtração
print(numero_1 - numero_2)  # -79
# Multiplicação
print(numero_1 * numero_2)  # 2100
# Divisão
print(numero_1 / numero_2)  # 0.21
# Podemos fazer expressões com várias operações
print(numero_3 * (numero_1 + numero_2))  # 60.5
# Módulo (resto da divisão)
print(numero_2 % numero_1)  # 16
# Exponenciação
print(numero_1 ** 3)  # 9261
# Divisão arredondada
print(numero_1 // 5)  # 4
